public class dowhile_loop {
    public static void main(String[] args) {
        int counter =1;
        do { 
            System.out.println("Hello World");
            ++counter;
        } while (counter<=10);
        
        counter=1;
        do { 
            System.out.println("\n\nHello World");
        } while (counter==0);
    }

}