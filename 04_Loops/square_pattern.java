public class square_pattern {
    public static void main(String[] args) {
        //Using for loop
        for (int line =1; line<=4; ++line) {
            System.out.println("****");
        }

        //Using while loop
        System.out.println("\n");
        int l=1;
        while (l<=4) {
            System.out.println("****");
            ++l;
        }
    }
}